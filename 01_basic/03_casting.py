
# Casting - Converting one data type to another

print("Conversion de tipo")

print(type("100")) # str
print(type(int("100"))) # int

print(bool("")) # False
print(bool(" ")) # True
print(bool(0)) # False
print(bool(1)) # True
print(bool([])) # False
print(bool([1, 2, 3])) # True

print(int(2.5)) # 2
print(int(2.9)) # 2

